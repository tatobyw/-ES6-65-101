let x = 5
let y = 3;

//Decrement
console.log(`--y = ${--y}`)//1-3 = 2
console.log(`y-- = ${y--}`)//2 ;return 2 then decrement by 1
console.log(`y = ${y}`)//2-1 = 1 
//increment
console.log(`++x = ${++x}`)//1+5 = 6
console.log(`x++ = ${x++}`)//return 6 then increment by 1
console.log(`x = ${x}`)//6+1 =7 

//remainder
console.log(`x % y = ${x % y}`)

//Muliplication
console.log(`x * y = ${x * y}`)

//subtraction
console.log(`x - y = ${x - y}`)

//addition
console.log(`x + y = ${x + y}`)